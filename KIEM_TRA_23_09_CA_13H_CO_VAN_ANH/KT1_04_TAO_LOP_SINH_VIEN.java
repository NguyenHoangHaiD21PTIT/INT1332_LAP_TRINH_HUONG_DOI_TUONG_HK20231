import java.util.*;
class Sinhvien{
    private String msv, ten, gioi, status, gioit;
    private double diem;
    public void input(){
        Scanner sc = new Scanner(System.in);
        this.msv = sc.nextLine().toUpperCase();
        this.ten = sc.nextLine();
        this.gioit = sc.nextLine();
        this.diem = sc.nextDouble();
        if(this.gioit.equals("True")||this.gioit.equals("true")){
            this.gioi = "nam";
        } else {
            this.gioi = "nu";
        }
        if(this.diem >=5){
            this.status = "dat";
        } else {
            this.status = "hoc lai";
        }
    }
    public void out(){
        System.out.println(this.msv + " " + this.ten + " " + this.gioi + " " + this.status);
    }
}
public class KT1_04_TAO_LOP_SINH_VIEN {
    public static void main(String[] args) {
        Sinhvien sv = new Sinhvien();
        //input()
        sv.input();
        //output()
        sv.out();
    }
}

